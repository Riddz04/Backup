#include<iostream>
using namespace std;
int main(){
    cout << "Welcome to the c++ world";
    cout <<endl;//enter ke liye use hota hai
    cout << "Lets start coding"<<endl;
    int x = 4;
    cout<<x<<endl;
    cout<<x++<<endl;//post increment
    cout<<x<<endl;
    int y = 5;
    cout<<y<<endl;
    cout<<++y<<endl;//pre increment
    cout<<y<<endl;
    return 0;
}